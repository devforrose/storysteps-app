export function getWordByText(text: string): Word | undefined {
  return vocabularyDatabase.find(
    (w) => w.text.toLowerCase() === text.toLowerCase() || w.lemma.toLowerCase() === text.toLowerCase()
  );
}

export function getWordById(id: string): Word | undefined {
  return vocabularyDatabase.find((w) => w.id === id);
}

export function getWordsByLevel(level: CEFRLevel): Word[] {
  return vocabularyDatabase.filter((w) => w.cefrLevel === level);
}
